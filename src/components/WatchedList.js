import average from "./../utils/averageFn";

export function WatchedList({ watched, onDeleteMovie }) {
    return (
        <ul className="list">
            {watched.map((movie) => (
                <WatchedMovie
                    movie={movie}
                    key={movie.imdbID}
                    onDeleteMovie={onDeleteMovie}
                />
            ))}
        </ul>
    );
}

